package calculations;

/**
 * Class that represents the values of the final calculations.
 */
public class CalculationValues {
    private String angle;
    private String roughness;
    private String t;
    private String qin;
    private String speed;
    private String idealt;
    private String surface;
    private String width;
    private String energy;
    private String froud;

    public String getAngle() {
        return angle;
    }

    public void setAngle(String angle) {
        this.angle = angle;
    }

    public String getRoughness() {
        return roughness;
    }

    public void setRoughness(String roughness) {
        this.roughness = roughness;
    }

    public String getT() {
        return t;
    }

    public void setT(String t) {
        this.t = t;
    }

    public String getQin() {
        return qin;
    }

    public void setQin(String qin) {
        this.qin = qin;
    }

    public String getSpeed() {
        return speed;
    }

    public void setSpeed(String speed) {
        this.speed = speed;
    }

    public String getIdealt() {
        return idealt;
    }

    public void setIdealt(String depth) {
        this.idealt = depth;
    }

    public String getSurface() {
        return surface;
    }

    public void setSurface(String surface) {
        this.surface = surface;
    }

    public String getWidth() {
        return width;
    }

    public void setWidth(String width) {
        this.width = width;
    }

    public String getEnergy() {
        return energy;
    }

    public void setEnergy(String energy) {
        this.energy = energy;
    }

    public String getFroud() {
        return froud;
    }

    public void setFroud(String froud) {
        this.froud = froud;
    }


}
