package calculations.model;

/**
 * Class that represents the ideal value of diameter, the ideal previous and next.
 */
public class DiameterValues {

    private double idealDiameter;
    private double idealPrevious;
    private double idealNext;

    public double getIdealDiameter() {
        return idealDiameter;
    }

    public void setIdealDiameter(double idealDiameter) {
        this.idealDiameter = idealDiameter;
    }

    public double getIdealPrevious() {
        return idealPrevious;
    }

    public void setIdealPrevious(double idealPrevious) {
        this.idealPrevious = idealPrevious;
    }

    public double getIdealNext() {
        return idealNext;
    }

    public void setIdealNext(double idealNext) {
        this.idealNext = idealNext;
    }
}
